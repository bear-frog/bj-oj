s = list(input())
ans = ''
cryptMap = {
    'a': 'n',
    'b': 'o',
    'c': 'p',
    'd': 'q',
    'e': 'r',
    'f': 's',
    'g': 't',
    'h': 'u',
    'i': 'v',
    'j': 'w',
    'k': 'x',
    'l': 'y',
    'm': 'z',
    'n': 'a',
    'o': 'b',
    'p': 'c',
    'q': 'd',
    'r': 'e',
    's': 'f',
    't': 'g',
    'u': 'h',
    'v': 'i',
    'w': 'j',
    'x': 'k',
    'y': 'l',
    'z': 'm'
}
for _s in s:
    if _s == ' ' or _s.isdigit():
        ans += _s
    elif _s.isupper():
        ans += cryptMap[_s.lower()].upper()
    elif _s.islower():
        ans += cryptMap[_s]

print(ans)